const ICON_BY_SLUG = new Map(HOST_ICONS.map((icon) => [icon.slug, icon]));

function withCurrentColor(raw) {
  return raw.replace('<svg ', '<svg fill="currentColor" ');
}

export function hostIconSvg(slug) {
  const icon = slug ? ICON_BY_SLUG.get(slug) : undefined;
  return icon ? withCurrentColor(icon.svg) : null;
}

export function HostIcon({ slug, fallback: Fallback, className }) {
  const svg = hostIconSvg(slug);
  if (!svg) return Fallback ? <Fallback className={className} /> : null;
  return (
    <span
      className={`inline-block [&>svg]:size-full ${className || ''}`}
      dangerouslySetInnerHTML={{ __html: svg }}
    />
  );
}
